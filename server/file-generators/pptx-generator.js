const pptxgen = require("pptxgenjs");

function toArr(v) {
  if (Array.isArray(v)) return v;
  if (v && typeof v === "object") return Object.values(v);
  return [];
}

async function generatePPTX(input, outputPath) {
  const { title, subtitle, theme_color, slides } = input;
  const color = (theme_color || "1E2761").replace("#", "");
  const safeSlides = toArr(slides);

  const pres = new pptxgen();
  pres.layout = "LAYOUT_16x9";
  pres.title = title || "Presentation";

  // Title slide
  const s1 = pres.addSlide();
  s1.background = { color };
  s1.addText(title || "Untitled", {
    x: 0.8, y: 1.5, w: 8.4, h: 1.5,
    fontSize: 38, fontFace: "Georgia", bold: true, color: "FFFFFF",
  });
  if (subtitle) {
    s1.addText(subtitle, {
      x: 0.8, y: 3.1, w: 7, h: 0.8,
      fontSize: 18, fontFace: "Calibri", color: "CADCFC",
    });
  }
  s1.addText(new Date().toLocaleDateString("en-US", { year: "numeric", month: "long" }), {
    x: 0.8, y: 4.5, w: 4, h: 0.4,
    fontSize: 12, fontFace: "Calibri", color: "8AAFC4",
  });

  // Content slides
  for (const slide of safeSlides) {
    const s = pres.addSlide();
    s.background = { color: "F0F7FA" };

    s.addShape(pres.shapes.RECTANGLE, {
      x: 0, y: 0, w: 10, h: 0.95, fill: { color },
    });
    s.addText(slide.title || "Slide", {
      x: 0.6, y: 0.12, w: 9, h: 0.7,
      fontSize: 26, fontFace: "Georgia", bold: true, color: "FFFFFF",
    });

    const bullets = toArr(slide.bullets);
    if (bullets.length) {
      s.addText(
        bullets.map((b, i) => ({
          text: String(b),
          options: { bullet: true, breakLine: i < bullets.length - 1 },
        })),
        {
          x: 0.8, y: 1.3, w: 8.4, h: 3.8,
          fontSize: 14, fontFace: "Calibri", color: "333333",
          paraSpaceAfter: 8,
        }
      );
    }
  }

  // Conclusion slide
  const sEnd = pres.addSlide();
  sEnd.background = { color };
  sEnd.addText("Thank You", {
    x: 0.8, y: 2.0, w: 8.4, h: 1.2,
    fontSize: 36, fontFace: "Georgia", bold: true, color: "FFFFFF",
  });
  sEnd.addText(`${safeSlides.length + 2} slides · Generated by AI Research Agent`, {
    x: 0.8, y: 3.5, w: 8, h: 0.5,
    fontSize: 12, fontFace: "Calibri", color: "8AAFC4",
  });

  await pres.writeFile({ fileName: outputPath });
  return outputPath;
}

module.exports = { generatePPTX };
